    private void tbDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDadosMouseClicked
        if (tbDados.getSelectedRow() >= 0){
            tfId.setText(tbDados.getModel().getValueAt(tbDados.convertRowIndexToModel(tbDados.getSelectedRow()), 0).toString());
            cbItinerario.setSelectedItem(tbDados.getModel().getValueAt(tbDados.convertRowIndexToModel(tbDados.getSelectedRow()), 1));
            tfNomePassageiro.setText(tbDados.getModel().getValueAt(tbDados.convertRowIndexToModel(tbDados.getSelectedRow()), 2).toString());
            tfTelefone.setText(tbDados.getModel().getValueAt(tbDados.convertRowIndexToModel(tbDados.getSelectedRow()), 3).toString());
            cbAssento.setSelectedIndex(Integer.valueOf(tbDados.getModel().getValueAt(tbDados.convertRowIndexToModel(tbDados.getSelectedRow()), 4).toString())-1);
        }
    }//GEN-LAST:event_tbDadosMouseClicked

    private void cbItinerarioItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbItinerarioItemStateChanged
        ItinerarioConversao conv = new ItinerarioConversao();
        assentoControl.getAssentoDigitado().setItinerario(conv.convertReverse(cbItinerario.getSelectedItem()));
        assentoControl.search();
    }//GEN-LAST:event_cbItinerarioItemStateChanged

